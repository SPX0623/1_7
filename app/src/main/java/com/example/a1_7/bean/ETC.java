package com.example.a1_7.bean;

public class ETC {
    private String carnum;
    private String czje;
    private String cztm;
    private int id;


    public int getId() {
        return id;
    }

    public ETC setId(int id) {
        this.id = id;
        return this;
    }

    public String getCarnum() {
        return carnum;
    }

    public ETC setCarnum(String carnum) {
        this.carnum = carnum;
        return this;
    }

    public String getCzje() {
        return czje;
    }

    public ETC setCzje(String czje) {
        this.czje = czje;
        return this;
    }

    public String getCztm() {
        return cztm;
    }

    public ETC setCztm(String cztm) {
        this.cztm = cztm;
        return this;
    }
}
